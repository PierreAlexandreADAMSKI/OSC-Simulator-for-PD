public class OSCPortException extends Throwable {
	public OSCPortException() {
		super();
	}
	public OSCPortException(Throwable cause) {
		super(cause);
	}
	public OSCPortException(String message) {
		super(message);
	}
	public OSCPortException(String message, Throwable cause) {
		super(message, cause);
	}
}
